num= int (input ('Digite um numero para descobrirmos se o mesmo é par ou ímpar: '))

if (num%2) == 0:
    print ('O numero que você digitou é par!')

else:
    print ('O numero que você digitou é impar!')


# Para saber o resto da divisão de um número por outro, usamos o operador %
#o operador % que é o operador de resto da divisão
#Para saber se um número é par ou ímpar, basta dividir ele por 2.
#Se for par, o resto é sempre 0, não sobra nada.
#Já se for ímpar, vai sempre ter resto 1.
